package com.cdi.com.CorferiasRuedaNegocios.ServiceImplements;

import com.cdi.com.CorferiasRuedaNegocios.Entity.LogConsolaEntity;
import com.cdi.com.CorferiasRuedaNegocios.Entity.TtTipoEnvioCorreoEntity;
import com.cdi.com.CorferiasRuedaNegocios.Repository.TtTipoEnvioCorreoRepository;
import com.cdi.com.CorferiasRuedaNegocios.Services.LogConsolaService;
import com.cdi.com.CorferiasRuedaNegocios.Services.TtTipoEnvioCorreoService;
import java.util.ArrayList;
import java.util.List;
import org.json.JSONObject;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class TtTipoEnvioCorreoServiceImplementacion implements TtTipoEnvioCorreoService {

    @Autowired
    private TtTipoEnvioCorreoRepository repository;

    @Autowired
    private LogConsolaService service;

    @Override
    public List<TtTipoEnvioCorreoEntity> listar() {
           try {
            return repository.findAll();
        } catch (Exception ex) {
            LogConsolaEntity entidadLog = new LogConsolaEntity();
            entidadLog.setCodigo(String.valueOf(ex.hashCode()));
            entidadLog.setMensaje(ex.getMessage());
            entidadLog.setServicio("Servicio TtTipoEnvioCorreo");
            entidadLog.setTipo(1);
            service.InsertaLog(entidadLog);
            List list = new ArrayList();
            list.add(0, JSONObject.quote("No fue posible ejecutar los datos, verifique el Log para validar la inconsistencia"));
            return list;

        }
    }

}
